$(document).ready(function() {
	$(".mc").css("visibility", "hidden");
	$(".mt").mouseenter(function() {
		$(".mc").css("visibility", "visible")
	});
	$(".mt").mouseleave(function() {
		$(".mc").css("visibility", "hidden")
	});
	$(".mc").mouseleave(function() {
		$(".mc").css("visibility", "hidden")
	});
	$(".mc").mouseenter(function() {
		$(".mc").css("visibility", "visible")
	})
});